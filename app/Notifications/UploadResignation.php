<?php

namespace App\Notifications;

use Illuminate\Bus\Queueable;
use Illuminate\Notifications\Notification;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;

class UploadResignation extends Notification
{
    use Queueable;

    /**
     * Create a new notification instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Get the notification's delivery channels.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function via($notifiable)
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return \Illuminate\Notifications\Messages\MailMessage
     */
    public function toMail($notifiable)
    {
        return (new MailMessage)
        ->greeting(' ')
        ->subject('Upload Resignation')
        ->line('We acknowledge receipt of your resignation letter.')
        ->line('The details you provided will be subject to confirmation by your immediate superior. Once confirmed, your resignation shall be processed. To facilitate your clearance, kindly check and settle your accountabilities, if there are any.')
        // ->action('(click button)', $this->server->server_ip.'/uploaded-letter')
        ->line('Please regularly check your e-mail for updates.')
        ->line('This is an auto generated email please do not reply')
        ->line('Thank you for using our application!');
    }

    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return [
            //
        ];
    }
}
